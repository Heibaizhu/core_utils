_reduction_modes = ['none', 'mean', 'sum']
_loss_modes = ['L1', 'L2']


@weighted_loss
def l1_loss(pred, target):
    return F.l1_loss(pred, target, reduction='none')


@weighted_loss
def mse_loss(pred, target):
    return F.mse_loss(pred, target, reduction='none')


@weighted_loss
def charbonnier_loss(pred, target, eps=1e-12):
    return torch.sqrt((pred - target)**2 + eps)


class AdaWeightedLoss(nn.Module):
    """Adaptive weighted loss.

    Args:
        loss_weight (float): Loss weight for loss. Default: 1.0.
        loss_mode (str): Specifies the loss mode for final calculation.
            Supported choices are 'L1' and 'L2'.
        inter_mode (str): Specifies the mode for interpolation .
            Supported choices are 'nearest', 'bilinear', and 'bicubic'
    """

    def __init__(self, loss_weight=1.0, loss_mode='L1', inter_mode='nearest'):
        super(AdaWeightedLoss, self).__init__()
        if loss_mode not in ['L1', 'L2']:
            raise ValueError(f'Unsupported type mode: {type}. '
                             f'Supported ones are: {_loss_modes}')
        self.loss_weight = loss_weight
        self.inter_mode = inter_mode
        if loss_mode == 'L1':
            self.adaptive_t = lambda x, y: (x-y).abs().mean(dim=(1, 2, 3),
                                                            keepdim=True)
            self.loss = l1_loss
        else:
            self.adaptive_t = lambda x, y: (x-y).square().mean(dim=(1, 2, 3),
                                                               keepdim=True)
            self.loss = mse_loss

    def forward(self, pred, target, src, **kwargs):
        """
        Args:
            src (Tensor): of shape (N, C, H/scale, W/scale). Source tensor.
            pred (Tensor): of shape (N, C, H, W). Predicted tensor.
            target (Tensor): of shape (N, C, H, W). Ground truth tensor.
        """
        with torch.no_grad():
            fake_target = F.interpolate(src,
                                        size=target.shape[2:],
                                        mode=self.inter_mode)
            weight = (fake_target - target).abs()
            weight = (weight - weight.amin(dim=[1, 2, 3], keepdim=True)) / \
                     (weight.amax(dim=[1, 2, 3], keepdim=True) -
                      weight.amin(dim=[1, 2, 3], keepdim=True) + 1e-6)
            t1 = self.adaptive_t(fake_target, target)
            t2 = self.adaptive_t(pred, target)
            adative_t = F.relu(1 - t2/(t1+1e-6))
            adaptive_weight = (1-adative_t) + weight * adative_t
        return self.loss_weight * self.loss(
            pred, target, adaptive_weight, reduction='mean')


class L1Loss(nn.Module):
    """L1 (mean absolute error, MAE) loss.

    Args:
        loss_weight (float): Loss weight for L1 loss. Default: 1.0.
        reduction (str): Specifies the reduction to apply to the output.
            Supported choices are 'none' | 'mean' | 'sum'. Default: 'mean'.
    """

    def __init__(self, loss_weight=1.0, reduction='mean'):
        super(L1Loss, self).__init__()
        if reduction not in ['none', 'mean', 'sum']:
            raise ValueError(f'Unsupported reduction mode: {reduction}. '
                             f'Supported ones are: {_reduction_modes}')

        self.loss_weight = loss_weight
        self.reduction = reduction

    def forward(self, pred, target, weight=None, **kwargs):
        """
        Args:
            pred (Tensor): of shape (N, C, H, W). Predicted tensor.
            target (Tensor): of shape (N, C, H, W). Ground truth tensor.
            weight (Tensor, optional): of shape (N, C, H, W). Element-wise
                weights. Default: None.
        """
        return self.loss_weight * l1_loss(
            pred, target, weight, reduction=self.reduction)


